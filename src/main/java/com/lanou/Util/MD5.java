public class MD5 {

    //如何用最快的方式实现5*8
    public static void main(String[] args) {
        //0101网左移三位
        System.out.println(5<<3);
        //八进制 以0开头
        System.out.println(0123);
        //16进制
        System.out.println(0x123);
        //&运算写成二进制 都为1 返回1  否则0
        //|运算写成二进制 都为0 返回0  否则1
        System.out.println(5 & 3);
        System.out.println(5 | 3);

        //现在有一个8进制的数 转换成 16进制

    }
}
